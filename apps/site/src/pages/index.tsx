import { lightFormat } from "date-fns";
import type { NextPage } from "next";
import Head from "next/head";

import { BottomNavigation } from "../components/BottomNavigation";
import { trpc } from "../utils/trpc";

const Page: NextPage = () => {
  const getNowQuery = trpc.logs.getNow.useQuery(
    {},
    {
      refetchInterval: false,
    }
  );

  return (
    <>
      <Head>
        <title>SolarSystem - Strona główna</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div id="container" className="flex flex-col w-full h-full bg-gray-200">
        <header className="flex items-center w-full h-32 gap-4 px-4 py-4 justify-evenly">
          <Card
            label="Moc teraz"
            value={getNowQuery.data?.power_now ?? 0}
            unit="W"
          />
          <Card
            label="Energia dziś"
            value={getNowQuery.data?.energy_today ?? 0}
            unit="kWh"
          />
          <Card
            label="Energia całość"
            value={getNowQuery.data?.energy_total ?? 0}
            unit="kWh"
          />
        </header>

        <div className="flex w-full flex-col items-center">
          <h2 className="text-xl">Data ostatniego pomiaru:</h2>
          <span className="font-bold">
            {lightFormat(
              getNowQuery.data?.timestamp ?? 0,
              "HH:mm:ss, dd.MM.yyyy"
            )}
          </span>
        </div>

        <main className="flex flex-col flex-1"></main>

        <BottomNavigation active="general" />
      </div>
    </>
  );
};

const Card: React.FC<{
  label: string;
  value: number | string;
  unit: string;
}> = (props) => {
  return (
    <div
      className={`flex flex-col items-center justify-center flex-1 h-full border-4 border-green-900 bg-green-300 rounded-xl`}
    >
      <div className="text-sm font-bold text-center">{`${props.value} (${props.unit})`}</div>
      <div className="text-sm text-center">{props.label}</div>
    </div>
  );
};

export default Page;
